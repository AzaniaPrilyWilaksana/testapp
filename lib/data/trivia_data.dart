    TriviaCategory? category,
    Difficulty? difficulty,
    int count = 10,
  }) {
    var pool = questions.toList();
    if (category != null) {
      pool = pool.where((q) => q.category == category).toList();
    }
    if (difficulty != null) {
      pool = pool.where((q) => q.difficulty == difficulty).toList();
    }
    pool.shuffle();
    return pool.take(count).toList();
  }
